-- 5. Final Analytics View (Medallion Architecture - Gold Layer)
CREATE OR REPLACE VIEW RETAIL_DWH.RAW_DATA.CUSTOMER_ORDER_SUMMARY AS
SELECT 
    C.CUSTOMER_ID,
    C.FIRST_NAME || ' ' || C.LAST_NAME AS FULL_NAME,
    COUNT(O.ORDER_ID) AS TOTAL_ORDERS,
    SUM(O.TOTAL_AMOUNT) AS TOTAL_SPENT
FROM RETAIL_DWH.RAW_DATA.STG_CUSTOMERS C
LEFT JOIN RETAIL_DWH.RAW_DATA.STG_ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
GROUP BY 1, 2;
