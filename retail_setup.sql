-- 1. Database మరియు Schema క్రియేషన్
CREATE OR REPLACE DATABASE RETAIL_DWH;
CREATE OR REPLACE SCHEMA RETAIL_DWH.RAW_DATA;

-- 2. Raw Customers Table (STAGING)
CREATE OR REPLACE TABLE RETAIL_DWH.RAW_DATA.STG_CUSTOMERS (
    CUSTOMER_ID INT,
    FIRST_NAME STRING,
    LAST_NAME STRING,
    EMAIL STRING,
    CITY STRING,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);

-- 3. Raw Orders Table (STAGING)
CREATE OR REPLACE TABLE RETAIL_DWH.RAW_DATA.STG_ORDERS (
    ORDER_ID INT,
    CUSTOMER_ID INT,
    ORDER_DATE DATE,
    TOTAL_AMOUNT DECIMAL(10,2),
    STATUS STRING
);

